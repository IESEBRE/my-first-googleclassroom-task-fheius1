package org.example;

public class OperadorsBoleans {

    public static void main(String [] arg) {
        boolean variable = true;
        boolean variabledos = false;

        //Per a fer la negacio
        System.out.println(variable);
        System.out.println(!variable);

        //Producte ilogic
        System.out.println( variable && variabledos);

        //Producte logic
        System.out.println(variable || variabledos);
    }
}
